def ham_tinh_chu_vi_dien_tich_hinh_tron_ginh_chu_nhat(r,a,b):
    S_tron=3.14*(r*r)
    print("dien tich hinh tron la:",S_tron)
    P_tron=2*r*3.14
    print("chu vi hinh tron la:",P_tron)
    S_chu_nhat=a*b
    print("dien tich hinh chu nhat la:",S_chu_nhat)
    P_chu_nhat=(a+b)*2
    print("chu vi hinh chu nhatr la:",P_chu_nhat)

ham_tinh_chu_vi_dien_tich_hinh_tron_ginh_chu_nhat(2,3,4)